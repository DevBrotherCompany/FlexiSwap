export function getRandomNFTItems(len: number) {
  const used: number[] = []
  const res: INftItem[] = []
  let rand: number
  for (let index = 0; index < len; index++) {
    rand = Math.floor(Math.random() * len)
    if (!used.includes(rand)) {
      used.push(rand)
      res.push(mocked_items[rand])
    }
  }
  return res
}
